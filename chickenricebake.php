                    <h1>Chicken and Rice Bake</h1>
                    <img class="mainPict" src="/media/chickenRiceBake.jpg" alt="Chicken and Rice in glass pan">                 <h3>Ingredients</h3> 
                    <ul>
                        <li>1 Tablespoon butter</li>
                        <li>1 cup chopped onion</li>
                        <li>1 cup chopped celery</li>
                        <li>2 1/2 cups Cream of Chicken soup - gluten free (or gluten free Cream of Mushroom soup</li>
                        <li>2 cups milk</li>
                        <li>1 small jar of chopped pimentos (optional)</li>
                        <li>Lawry's seasoning (or a seasoning mix of your choice and add paprika)</li>
                        <li>Salt & Pepper</li>
                        <li>Boneless Chicken Breasts cut lengthwise in half</li>
                     </ul>   
                    <h3>Directions</h3>
                    <ol>
                        <li>In a medium size skillet, melt butter over medium heat.</li>
                        <li>Add onion and celery and saute over medium heat until tender.</li>
                        <li>Mix Cream of Chicken soup, milk and pimentos.</li>
                        <li>Add rice to bottom of 9 x 13 inch baking dish.</li>
                        <li>Stir in soup/milk/pimento mixture.</li>
                        <li>Distribute celery and onions over everything.</li>
                        <li>Sprinkle Sawry's seasoning salt over mixture.</li>
                        <li>Lay 8 Chicken breast pieces over the top.</li>
                        <li>Season with salt and pepper.</li>
                        <li>Cover with aluminum foil and bake for 40 minutes at 350 degrees.</li>
                        <li>Remove aluminum foil and bake an additional 15 minutes or until chicken is done.</li>
                        <li>Remove from oven, sprinkle with grated Parmesan cheese</li>
                            
                    </ol>
     

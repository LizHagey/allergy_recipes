                    <h1>Gluten Free Bread</h1>
                    <img class = "mainPict" src="/media/BreadSlice.jpg" alt="Gluten Free Bread">
                    <p>Who knew you could have delicious bread that is gluten free.  Try this amazing recipe!</p>
                    
               
                <h4>Directions</h4>
                <ul>
                    <li>1 Cup Warm almond milk</li>
                    <li>1 Tablespoon of yeast</li>
                    <li>1/2 Cup of apple juice (any type of juice will work, or just water will work)</li>
                    <li>3 eggs</li>
                    <li>1/3 cup olive oil</li>
                    <li>1/3 cup honey</li>
                </ul>
                <ul>
                    <li>Mix the above ingredients together</li>
                </ul>
                <ul>
                    <li>Add</li>
                    <li>2 cups brown rice flour</li>
                    <li>1 cup almond flour</li>
                    <li>1 tablespoon xanthun gum </li>
                    <li><img src='media/Gum.jpg' alt="Bottle of Xanthun Gum"></li>
                    <li>1 teaspoon apple cider vinegar</li>
                    <li>1/4 teaspoon salt</li>
                </ul>
                <p>Mix ingredients well.</p>
                <img src='media/mixDough.jpg' alt='Dough Mixture'>
                <p>Put dough in a bread pan, cover with a damp towel and let rise until dough reaches the top of the bread pan.</p>
                <img src='media/LetRise.jpg' alt='Dough in pan'>
               
                <ul>
                     <li>Bake 350 degrees for 20 minutes</li>
                    <li>Cover bread with tinfoil so it won't burn</li>
                    <li>Continue to bake for another 20 minutes or until done. (You can use a meat thermometer and when it reaches 150 degrees or higher when inserted the bread is done). </li>
                </ul>
                
                <img src='media/Loaf.jpg' alt='Loaf of bread'>
                <p>Makes 1 loaf</p>
                <h4>Notes about this bread:</h4>
                <p><em>Amaranth flour:</em> I have used this instead of almond flour and the bread was much heavier and I did not like it.</p>
                <p><em>Hemp Seed Flour: </em>I replaced the cup of almond flour with the hemp seed flour and loved it because hemp seed flour holds the moisture in.  The brad was so moist that hemp seed flour was my favorite flour in this recipe.  Unfortunately I was not used to hemp seed and got sick from it.  So use hemp seed flour at your own risk or try a mixture of hemp seed and the almond flour so you don't get sick.  I haven't dared try again.</p>
                <p><em>Brown Rice Flour:</em> I use my blendtec - I put the brown rice in and hit the whole juice button.  A couple of minutes later I have the flour.</p>
                <img src='media/Blend.jpg' alt='Blending rice in blendtec'>
                <p><em>Almond Flour:</em> I use my blendtec - I put the almonds in and hit the whole juice button but I have to stand by the blendtec  and turn it off manually because the oils in the almonds binds the flour together and I need to manually turn off the blendtec. I don't want to ruin my blendtec. The almond flour I get from the blendtec  has a cornmeal consistency and that is what I use in my bread. It works well and tastes great and the bread holds together and doesn't crumble like a lot of other gluten free breads.</p>

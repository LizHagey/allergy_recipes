                <p>In order to cook Gluten Free it helps if you understand what gluten is.<br><br>

                    Gluten is two proteins that are found in wheat and other related grains such as barley and rye. The proteins  which are gliadin ( a prolamin protein)  and glutenin (a glutelin protein) are found in the wheat endosperm or seed. 
                    <br><br>The proteins are not found in the grass itself which is why you could still have wheat grass juice if you were careful to never get the grain or seed in the juice.<br><br>

                    Gluten when it gets wet becomes elastic and stretchy while still being strong.  <br><br>
                    Gluten's name came from the glue-like qualities it possesses. <br><br>
                    When yeast is added the gases from the yeast are stopped by the gluten. This causes the dough to rise. <br><br>

                    In essence gluten is what gives a light and fluffy feel to food. It is also what makes food chewy. <br><br>
                    The amount of  water added and the amount of stirring is what causes more elasticity and chewiness in the food. For instance a cake does not need as much elasticity as bread so less water and kneading  would be done with a cake. A pie crust needs to flake and break apart fairly easy, so it needs even less water and stirring than cake.<br><br>

                    Also it is important to understand that the wheat seed has starch in it and the starch helps ingredients bind together.<br><br>


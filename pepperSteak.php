                    <h1>Pepper Steak Supreme</h1>
                    <img class="mainPict" src="/media/pepperSteak.jpg" alt="Pepper Steak Supreme">
                    <p>Gluten free main dish.  Best when served over rice.</p>
                
                <h4>Meat and Broth</h4>
                <ul>
                    <li>2 Tablespoons Butter</li>
                    <li>1 lb. Beef Stew Meat</li>
                    <li>Salt and Pepper</li>
                    <li>4 cups Water (or Beef Broth)</li>
                </ul>
                <h4>Gravy</h4>
                <ul>
                    <li>2 - 3 Green Bell Peppers</li>
                    <li>16 oz. Mushrooms</li>
                    <li>1 Large Yellow Onion</li>
                    <li>2 Garlic Cloves</li>
                    <li>3 Tablespoons Cornstarch</li>
                    <li>3 Tablespoons Cold Water</li>
                    <li>3/4 cup Butter</li>
                    <li>1/2 teaspoon Worcestershire Sauce</li>
                    <li>1/2 teaspoon Pepper</li>
                    <li>1 teaspoon Salt</li>
                </ul>
                <ol>
                    <li>Salt and Pepper Stew Meat</li>
                    <li>Cut Bell Peppers into bite-size pieces</li>
                    <li>Slice Mushrooms</li>
                    <li>Dice Yellow Onion</li>
                    <li>Mince Garlic</li>
                    <li>Whisk together cornstarch and the cold water in a small bowl</li>
                    <li>Bring Water to Boil in a saucepan, leaving enough room to add in all your meat. This is to make the broth for the gravy if you do not already have any beef broth.</li>
                    <li>Melt 2 Tablespoons butter in a large frying pan, then</li>
                    <li>Add 1 Tablespoon diced onion and stew meat;</li>
                    <li>Sear on each side, then quickly add all of that to your boiling water;</li>
                    <li>Return frying pan to medium-high heat;</li>
                    <li>Melt 3/4 cup butter</li>
                    <li>Add onions, let cook for 2-3 minutes;</li>
                    <li>Add Green Peppers and Mushrooms; stir occasionally; let those cook until crisp but tender (not soggy);</li>
                    <li>Remove the Boiling Beef from heat, it should be cooked through now; (if small pieces do not let boil too long or it will get tough) Now you should have a nice broth for the gravy.</li>
                    <li>Strain the stew meat;</li>
                    <li>Add to the Frying Pan (I just use a slotted spoon and fish the pieces out);</li>
                    <li>Add Garlic, Salt, Pepper, and Worcestershire Sauce; stirring constantly for 1 minute;</li>
                    <li>Add broth to the frying pan; keep stirring; bring to a boil;</li>
                    <li>Grab your whisk and whisk the cornstarch and water mixture into the frying pan; pour slowly;</li>
                    <li>Boil for 1 minute; stirring constantly. The broth should now be gravy. Remove from heat;</li>
                    <li>Serve hot over rice.</li>
                </ol>
                <h4>Tips:</h4>
                <ul>
                    <li>Test the flavor before you add the cornstarch and adjust.</li>
                    <li>If not making a beef broth melt all the butter, add in the onion and the meat at the same time, then add all the other vegetables once meat has been seared on all sides.</li>
                    <li>Cook the vegetables to your preference , but saut√©ing them before adding the broth releases their flavor into the gravy.</li>
                </ul>
                

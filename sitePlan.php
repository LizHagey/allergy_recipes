                <h2>Asset/Content List</h2>
                <ul>
                    <li><strong>Home Page:</strong> Home page that will make clear the purpose of the site, have a navigation bar that will lead the user to other pages of the website.  The navigation bar will have: Gluten free recipes, tips, Information, and about me.  It will have a logo with a banner at the top of the page.  It will have 8 pictures of the delicious food that they can click on to find the recipe.  Underneath each picture will be a short text stating what it is and something enticing about it.  The pictures and text will be in two columns with a box outline around the picture and text so that the user will know what picture the text is talking about.</li>
                    <li><strong>All of the sub-pages</strong> will have the same logo, banner and navigation bar at the top of the page.  They will also have 2 pictures like on the home page that have the box around the text and picture.  These pictures will be links to those pages with those recipes.</li>
                    <li><strong>Sub-page layer 1 - Information: </strong>When they click on the link in the navigation bar for information on gluten intolerance they will come to this page that will be a well written article about gluten intolerance and a personal story with possible links to other articles.  – <em>Scenario 2 & 10</em></li>
                    <li><strong>Sub-page layer 1 - Tips: </strong>When they click on the link in the navigation bar for tips for living gluten free they will come to a page that has tips from other people who have suffered from the same problem.  This will be like a bullet list and will have the name of the person who offered the tip next to each tip.  This will have some good gluten free lunch ideas and ways to make cooking easier without having to buy expensive specialty ingredients.  This will have an audio recording of someone giving tips on the use of different types of flour.  – <em>Scenario 1,3,9 & 10</em></li>
                    <li><strong>Sub-page layer 1 - Gluten Free Recipes: </strong>When they click on gluten free recipes they will come to a page that has pictures of food that represent different categories.  These pictures will each have a title and the picture and title will be links to new pages.  The titles will be: Bread, Snacks, Main Dish, Desserts and Easy.  There will also be a recipe underneath these categories with a title, “Try our latest recipe” or something catchy.  This will then have a picture of the food along with the recipe.</li>
                    <li><strong>Sub-page layer 2 - Bread: </strong>When the Bread in the gluten free recipe page is clicked it will come to a page with pictures of two options: Gluten Free Bread or Gluten free waffles along with pictures.</li>
                    <li><strong>Sub-page layer 2 - Main Dish: </strong>When the main dish page on the gluten free recipe page is clicked it will come to a page showing a recipe along with pictures and instructions <em>Scenario 1 & 6</em></li>
                    <li><strong>Sub-page layer 2 - Desserts: </strong>When the dessert in the gluten free recipe page is clicked it will come to a page with pictures of the dessert, a list of the ingredients and directions on how to make it.  This page will have a video of someone making it. - <em>Scenario 7 & 8</em></li>
                    <li><strong>Sub-page layer 2 - Snacks: </strong>When the snack in the gluten free recipe page is clicked it will come to a page with pictures of the snack (protein balls), pictures of some of the ingredients, pictures of the steps and text of the recipe.  – <em>Scenario 4, 5 & 6</em></li>
                    <li><strong>Sub-page layer 2 - Easy: </strong>When the easy link in the gluten free recipe page is clicked it will come to a page with pictures of 4 easy main dishes with pictures of each dish being a link to the page that has the recipe for that dish. - <em>Scenario 1,3 & 4</em></li>
                    <li>All pages will have a footer</li>
                </ul>

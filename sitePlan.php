                <h1>Site Plan</h1>
                <h2>Site Description</h2>
                <p>This site will be a place where people can go to find recipes that do not have gluten in them.  It will also be a place where they can learn tips and other information about gluten intolerance.</p>
                <h2>Site Name</h2>
                <p>The name of this site is www.topallergyfreerecipes.com. </p>
                <h2>Site Purpose</h2>
                <p>The main purpose of topallergyfreerecipes.com website is to have a place for food allergy sufferers (specifically gluten) to find recipes that they can cook and eat without getting sick.  It is also a place to educate people about the different options they have for substituting wheat and white flour.  They will learn tips on how others have dealt with their allergies as well.</p>
                <h2>Target Audience</h2>
                <p>My target audience are people who are suffering from food allergies or have children that are suffering from food allergies.  This is mostly women who are health conscience out of need due to allergies or because they are concerned about their health.  </p>
                <p>Age: 20-70</p>
                <p>Gender: Mostly Women</p>
                <p>Tech savy: This group of people are often on the computer and frequently use social media.</p>
                <p>Education level: High school graduate – Masters or more – I would say more of my target audience is college educated</p>
                <p>Economics: They are mostly middle to upper class.</p>
